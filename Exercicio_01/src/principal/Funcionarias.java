package principal;

public class Funcionarias {
	private float pes;
	private float maos;
	private float podologia;
	
	public void setPes(float pes) {
		this.pes = pes;
	}
	
	public float getPes() {
		return this.pes;
	}
	
	public void setMaos(float maos) {
		this.maos = maos;
	}
	
	public float getMaos() {
		return this.maos;
	}
	
	public void setPodologia(float podologia) {
		this.podologia = podologia;
	}
	
	public float getPodologia() {
		return this.podologia;
	}
	
}


